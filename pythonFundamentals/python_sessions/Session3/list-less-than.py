
"""
This module creates list of values less than 20 and greater than 10

- list
- while
- conditionals
"""

### Create a empty list
container = []

# Set initial value to 1
# Assign 1 to variable num
num = 1

# Loop and increment num untill it reaches 20
while num < 20:
    # pass
    if num > 10:
        container.append(num)
    num += 1

print(container)





